package src.arrays;


public class ordemInversa {

    public static void main(String[] args) {
        
        int[] vetor = {0, 5, 8, -12, 4, 7};

        
        
        System.out.println("Vetor: ");
        int count = 0;
        while(count < (vetor.length)) {

            System.out.println(vetor[count] + " ");
        count++;
        }


    }
    
}
